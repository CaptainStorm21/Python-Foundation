# A Tuple is a collection which is ordered and unchangeable. Allows duplicate members.
 
 
 # simple tuple
fruitTuple = ('Apple', 'Orange', 'Mango')
print(fruitTuple)
print(fruitTuple(1))

# using a constructor
fruitTuple1 = tuple(('Blueberry", ''Apple', 'Orange', 'Mango'))
print(fruitTuple1)


#Get a single value
print(fruitTuple1[3])

